class Vehicle {
  final int markOfMastery;
  final int wins;
  final int battles;
  final String description;
  final String image;
  final bool isPremium;
  final String name;
  final String nation;
  final String type;
  final int tier;
  final bool isGift;

  Vehicle({
    required this.markOfMastery,
    required this.wins,
    required this.battles,
    required this.description,
    required this.image,
    required this.isPremium,
    required this.name,
    required this.nation,
    required this.type,
    required this.tier,
    required this.isGift,
  });
}
